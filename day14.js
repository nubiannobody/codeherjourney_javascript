/* Have you heard of switch statements in JavaScript?
What will the following code print to the console? */

let programmingLanguage = "PHP";

switch (programmingLanguage) {
    case "JavaScript":
        console.log("Courses start from $4");
        break;
    case "Python":
        console.log("Courses start from $5");
        break;
    default:
        console.log("No courses currently available");
}